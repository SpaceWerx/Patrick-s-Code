package com.revature.model;

public class Calculator {

	//We are going to make 3 really simply methods to test with Junit
	
	//addition
	public int add(int x, int y) {
		return x + y;
	}
	//subtraction
	public int subtract(int x, int y) {
		return x - y;
	}
	//division
	public int divide(int x, int y) {
		return x / y;
	}
}
